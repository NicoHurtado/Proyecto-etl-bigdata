CREATE TABLE LocationInfo (
    location_id SERIAL PRIMARY KEY,
    latitude DECIMAL(9, 6) NOT NULL,
    longitude DECIMAL(9, 6) NOT NULL,
    generationtime_ms DECIMAL(10, 5),
    utc_offset_seconds INT,
    timezone VARCHAR(255),
    timezone_abbreviation VARCHAR(50),
    elevation DECIMAL(10, 2),
    data_retrieval_timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

COMMENT ON TABLE LocationInfo IS 'Almacena metadatos de la ubicación y la consulta a la API.';
COMMENT ON COLUMN LocationInfo.location_id IS 'Identificador único autoincremental para la ubicación/consulta.';
COMMENT ON COLUMN LocationInfo.latitude IS 'Latitud de la ubicación.';
COMMENT ON COLUMN LocationInfo.longitude IS 'Longitud de la ubicación.';
COMMENT ON COLUMN LocationInfo.generationtime_ms IS 'Tiempo de generación de datos por la API en milisegundos.';
COMMENT ON COLUMN LocationInfo.utc_offset_seconds IS 'Desplazamiento UTC en segundos.';
COMMENT ON COLUMN LocationInfo.timezone IS 'Nombre de la zona horaria (ej: America/Bogota).';
COMMENT ON COLUMN LocationInfo.timezone_abbreviation IS 'Abreviatura de la zona horaria (ej: GMT-5).';
COMMENT ON COLUMN LocationInfo.elevation IS 'Elevación del lugar en metros.';
COMMENT ON COLUMN LocationInfo.data_retrieval_timestamp IS 'Fecha y hora en que se insertó el registro en la BD.';

-- -----------------------------------------------------
-- Table DailyWeather
-- Almacena los registros meteorológicos diarios
-- -----------------------------------------------------
CREATE TABLE DailyWeather (
    weather_id SERIAL PRIMARY KEY,
    observation_date DATE NOT NULL,
    temperature_2m_max DECIMAL(5, 2), 
    precipitation_sum DECIMAL(5, 2),  
    temperature_unit VARCHAR(10),    
    precipitation_unit VARCHAR(10),   
    CONSTRAINT fk_dailyweather_locationinfo
        FOREIGN KEY (location_id)
        REFERENCES LocationInfo (location_id)
        ON DELETE CASCADE, 
    UNIQUE (location_id, observation_date) 
);

COMMENT ON TABLE DailyWeather IS 'Almacena los registros meteorológicos diarios.';
COMMENT ON COLUMN DailyWeather.weather_id IS 'Identificador único autoincremental para el registro meteorológico diario.';
COMMENT ON COLUMN DailyWeather.location_id IS 'Referencia a la tabla LocationInfo.';
COMMENT ON COLUMN DailyWeather.observation_date IS 'Fecha del registro meteorológico.';
COMMENT ON COLUMN DailyWeather.temperature_2m_max IS 'Temperatura máxima a 2 metros.';
COMMENT ON COLUMN DailyWeather.precipitation_sum IS 'Suma de precipitación diaria.';
COMMENT ON COLUMN DailyWeather.temperature_unit IS 'Unidad de la temperatura (ej: °C).';
COMMENT ON COLUMN DailyWeather.precipitation_unit IS 'Unidad de la precipitación (ej: mm).';
COMMENT ON CONSTRAINT fk_dailyweather_locationinfo ON DailyWeather IS 'Clave foránea que relaciona el dato diario con su ubicación.';



